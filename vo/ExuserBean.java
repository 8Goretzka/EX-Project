package vo;

public class ExuserBean {
	private String EXUSER_ID;
	private String EXUSER_PASS;
	private String EXUSER_NAME;
	private int EXUSER_AGE;
	private String EXUSER_GENDER;
	private String EXUSER_EMAIL;
	
	public String getEXUSER_ID() {
		return EXUSER_ID;
	}
	public void setEXUSER_ID(String exuser_id) {
		EXUSER_ID = exuser_id;
	}
	public String getEXUSER_PASS() {
		return EXUSER_PASS;
	}
	public void setEXUSER_PASS(String exuser_pass) {
		EXUSER_PASS = exuser_pass;
	}
	public String getEXUSER_NAME() {
		return EXUSER_NAME;
	}
	public void setEXUSER_NAME(String exuser_name) {
		EXUSER_NAME = exuser_name;
	}
	public int getEXUSER_AGE() {
		return EXUSER_AGE;
	}
	public void setEXUSER_AGE(int exuser_age) {
		EXUSER_AGE = exuser_age;
	}
	public String getEXUSER_GENDER() {
		return EXUSER_GENDER;
	}
	public void setEXUSER_GENDER(String exuser_gender) {
		EXUSER_GENDER = exuser_gender;
	}
	public String getEXUSER_EMAIL() {
		return EXUSER_EMAIL;
	}
	public void setEXUSER_EMAIL(String exuser_email) {
		EXUSER_EMAIL = exuser_email;
	}
}
